"""
    函数参数
        实际参数:与形参对应
            位置实参:按顺序
                序列实参:拆序列
            关键字实参:按名字
                字典实参:拆字典
"""

def func01(p1,p2,p3):
    print(p1)
    print(p2)
    print(p3)

#   1.位置实参:按照顺序,与形参对应
func01(1,2,3)

#   2.序列实参:* --> 将序列拆分后按照顺序与形参对应
#        价值:统一管理数据
list=["a","b","c"]
func01(*list) # 一 --> 多

#   3.关键字实参:按照名字,与形参对应
#     价值:
func01(p1=1,p2=2,p3=3)

#   4.字典实参:将字典拆分后,按照名字,与形参对应
dict01 = {"p1":1,"p2":2,"p3":3}
func01(**dict01)
